#include "libft.h"

void	*ft_bzero(void	*s, size_t	n)
{
	size_t			i;
	unsigned char	*c;

	c = s;
	i = 0;
	while (i < n)
	{
		c[i] = '\0';
		i++;
	}
	return (c);
}
